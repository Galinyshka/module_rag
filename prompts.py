CLASSIFY_PROMPT = """\
Ты классифицируешь запрос к корпусу РПД.

Запрос пользователя:
{query}

Найденные дисциплины:
{disciplines}

Типы:

single.simple
- одна дисциплина
- запрос конкретного факта:
  часы, ЗЕ, темы, литература, компетенции,
  форма контроля, экзамен/зачёт, список и т.п.

single.global
- одна дисциплина
- обзор / описание / анализ / чему учит /
  как устроен курс / расскажи подробно

multi.relation
- две или более дисциплины
- сравнение / отличие / связь / пересечения

multi.global
- дисциплины не указаны
- поиск по всему корпусу:
  какие курсы, где изучается, в каких дисциплинах,
  есть ли дисциплины с ..., все дисциплины и т.п.

not_found
- пользователь явно спрашивает про конкретную дисциплину,
  но такой дисциплины нет в списке

Правила:

1. Если найдено 2+ дисциплины -> multi.relation

2. Если найдена 1 дисциплина:
   - если вопрос про конкретный факт -> single.simple
   - иначе -> single.global

3. Если найдено 0 дисциплин:
   - если запрос похож на поиск по корпусу -> multi.global
   - если пользователь спрашивает конкретную дисциплину -> not_found

Отвечай только JSON:

{{
  "query_type": "single.simple|single.global|multi.relation|multi.global|not_found",
  "disciplines": []
}}
"""

